    def call_openai_with_retry(self, model_id, task_name, text=None, sentence1=None, sentence2=None, max_retries=3):
        """Call OpenAI API with retry logic using multi-turn conversation format"""

        # Get task-specific messages with few-shot examples
        messages = self.get_messages_for_task(task_name, text=text, sentence1=sentence1, sentence2=sentence2)

        for attempt in range(max_retries):
            try:
                response = self.client.chat.completions.create(
                    model=model_id,
                    messages=messages,
                    max_tokens=2,
                    temperature=0.0,
                    timeout=30
                )

                answer = response.choices[0].message.content.strip()

                # Parse response
                if answer == '0':
                    return 0
                elif answer == '1':
                    return 1
                elif '0' in answer and '1' not in answer:
                    return 0
                elif '1' in answer and '0' not in answer:
                    return 1
                else:
                    # Unclear response - retry with SAME model and SAME prompt
                    if attempt < max_retries - 1:
                        print(f"  üîÑ {model_id} retry {attempt + 1}: '{answer}' unclear, retrying same model...")
                        time.sleep(1.0)  # Wait a bit longer before retry
                        continue
                    else:
                        print(f"  ‚ùå {model_id} final attempt failed: '{answer}', defaulting to 0")
                        return 0

            except Exception as e:
                error_str = str(e)
                if "rate_limit" in error_str.lower() or "429" in error_str:
                    print(f"  üîÑ {model_id} rate limit, waiting and retrying...")
                    time.sleep(3)
                    continue
                elif "insufficient" in error_str.lower() or "quota" in error_str.lower():
                    print(f"  üí≥ {model_id} insufficient credits")
                    return None
                elif attempt < max_retries - 1:
                    print(f"  üîÑ {model_id} retry {attempt + 1}: {str(e)[:50]}...")
                    time.sleep(2)
                    continue
                else:
                    print(f"  ‚ùå {model_id} final error: {e}")
                    return 0

        return 0

