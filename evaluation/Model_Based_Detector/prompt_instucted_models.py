def load_data(file_path):
    """Load data from JSON file"""
    print(f"üìÇ Loading data from: {file_path}")

    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
    except Exception as e:
        print(f"‚ùå Error reading file {file_path}: {e}")
        return []

    # Debug: Print data type and structure
    print(f"üîç Data type: {type(data)}")
    if isinstance(data, dict):
        print(f"üîç Dict keys: {list(data.keys())}")
        if len(data) > 0:
            first_key = list(data.keys())[0]
            print(f"üîç First key '{first_key}' type: {type(data[first_key])}")
            if isinstance(data[first_key], list) and len(data[first_key]) > 0:
                print(f"üîç First item in '{first_key}': {type(data[first_key][0])}")

    # Ensure data is a list
    if not isinstance(data, list):
        print(f"‚ö†Ô∏è Warning: Data is not a list, type: {type(data)}")
        if isinstance(data, dict):
            # If it's a dict, try to find the actual data
            if 'data' in data:
                data = data['data']
                print("üîç Found 'data' key, using data['data']")
            elif 'samples' in data:
                data = data['samples']
                print("üîç Found 'samples' key, using data['samples']")
            elif 'items' in data:
                data = data['items']
                print("üîç Found 'items' key, using data['items']")
            else:
                print(f"‚ùå Cannot convert data to list. Keys: {list(data.keys()) if isinstance(data, dict) else 'N/A'}")
                # Try to use the first value if it's a list
                for key, value in data.items():
                    if isinstance(value, list):
                        print(f"üîç Using key '{key}' which contains a list")
                        data = value
                        break
                else:
                    return []
        else:
            print(f"‚ùå Unexpected data type: {type(data)}")
            return []

    # Final check
    if not isinstance(data, list):
        print(f"‚ùå Still not a list after conversion: {type(data)}")
        return []

    print(f"‚úÖ Loaded {len(data)} samples")

    # Check data format and show sample
    if len(data) > 0:
        print("\nüìã Sample data:")
        for i, sample in enumerate(data[:3]):
            print(f"  {i}: {sample}")

        # Check label distribution
        if isinstance(data[0], dict) and 'label' in data[0]:
            labels = [item['label'] for item in data]
            human_count = sum(1 for label in labels if label == 0)
            llm_count = sum(1 for label in labels if label == 1)

            print(f"\nüìä Label distribution:")
            print(f"  Human (0): {human_count} samples ({human_count/len(data)*100:.1f}%)")
            print(f"  LLM (1): {llm_count} samples ({llm_count/len(data)*100:.1f}%)")

    return data

